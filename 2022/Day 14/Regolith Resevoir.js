function processLine(line)
{
    let commands = line.toString().split(" -> ");

    //prime loop
    let command = commands.shift().split(",");
    let current = [parseInt(command[0] - adjust), parseInt(command[1])];
    command = commands.shift().split(",");
    let next = [parseInt(command[0] - adjust), parseInt(command[1])];

    //do work
    adjustCave(current, next);

    while(commands.length)
    {
        current = next;
        command = commands.shift().split(",");
        next = [parseInt(command[0] - adjust), parseInt(command[1])];

        adjustCave(current, next);
    }
}

function adjustCave(curr, next)
{
    let curr_x = curr[0];
    let curr_y = curr[1];

    let next_x = next[0];
    let next_y = next[1];

    //vertical
    if(curr_y >= next_y)
    {
        while(curr_y >= next_y)
        {
            cave[curr_y][curr_x] = '#';
            curr_y--;
        }
    }
    else
    {
        while(curr_y <= next_y)
        {
            cave[curr_y][curr_x] = '#';
            curr_y++;
        }
    }

    curr_x = curr[0];
    curr_y = curr[1];

    next_x = next[0];
    next_y = next[1];

    //horizontal
    if(curr_x >= next_x)
    {
        while(curr_x >= next_x)
        {
            cave[curr_y][curr_x] = '#';
            curr_x--;
        }
    }
    else
    {
        while(curr_x <= next_x)
        {
            cave[curr_y][curr_x] = '#';
            curr_x++;
        }
    }
}
