function fallDownwards(sand)
{
    while(canFallDownwards(sand))
    {
        sand.y++;
    }

    return sand;
}

function canFallDownwards(sand)
{
    let checkY = sand.y + 1;
    let checkX = sand.x;
    if(isInBounds(checkY))
    {
        return (cave[checkY][checkX] != '#' && cave[checkY][checkX] != 'o');
    }
    else
    {
        return false;
    }
}

function createTable(tableData)
{
    var table = document.createElement('table');
    var tableBody = document.createElement('tbody');
  
    tableData.forEach(function(rowData)
    {
        var row = document.createElement('tr');

        rowData.forEach(function(cellData)
        {
            var cell = document.createElement('td');
            cell.appendChild(document.createTextNode(cellData));
            row.appendChild(cell);
        });
  
        tableBody.appendChild(row);
    });
  
    table.appendChild(tableBody);
    document.body.appendChild(table);
}

function findFloor()
{
    let floor = 0;
    for(var i = 0; i < cave.length; i++)
    {
        for(var j = 0; j < cave[i].length; j++)
        {
            if(cave[i][j] == '#')
            {
                if(i > floor)
                {
                    floor = i;
                }
            }
        }
    }

    return floor + 2;
}